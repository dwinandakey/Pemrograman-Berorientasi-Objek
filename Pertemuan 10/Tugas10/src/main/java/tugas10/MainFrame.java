    private void SaveMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveMenuItemActionPerformed
        // TODO add your handling code here:
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Text Files (*.txt)", "txt"));
        if (fileChooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            if (!file.getName().toLowerCase().endsWith(".txt")) {
                file = new File(file.getAbsolutePath() + ".txt");
            }
            try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
                writer.write(InputTextArea.getText());
                JOptionPane.showMessageDialog(this, "Text successfully saved to " + file.getPath());
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error saving file: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_SaveMenuItemActionPerformed

    private void SaveProsesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveProsesButtonActionPerformed
        // TODO add your handling code here:
        if (KarakterTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please process the text first.");
        } else {
            fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Text Files (*.txt)", "txt"));
            if (fileChooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
                File file = fileChooser.getSelectedFile();
                if (!file.getName().toLowerCase().endsWith(".txt")) {
                    file = new File(file.getAbsolutePath() + ".txt");
                }
                try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
                    writer.write("Hasil Proses Text\n");
                    writer.write("=================\n");
                    writer.write("Jumlah Karakter (diluar spasi) : " + KarakterTextField.getText() + "\n");
                    writer.write("Jumlah Baris : " + BarisTextField.getText() + "\n");
                    writer.write("Jumlah Huruf Vokal : " + VokalTextField.getText() + "\n");
                    writer.write("Jumlah Huruf Konsonan : " + KonsonanTextField.getText() + "\n");
                    JOptionPane.showMessageDialog(this, "Processed text successfully saved to " + file.getPath());
                } catch (IOException e) {
                    JOptionPane.showMessageDialog(this, "Error saving file: " + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_SaveProsesButtonActionPerformed

