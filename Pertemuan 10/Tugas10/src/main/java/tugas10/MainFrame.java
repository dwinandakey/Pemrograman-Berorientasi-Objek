    private void OpenMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenMenuItemActionPerformed
        // TODO add your handling code here:
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Text Files (*.txt)", "txt"));
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            if (file != null && file.getName().toLowerCase().endsWith(".txt")) {
                try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
                    StringBuilder content = new StringBuilder();
                    String line;
                    while ((line = reader.readLine()) != null) {
                        content.append(line).append("\n");
                    }
                    InputTextArea.setText(content.toString());
                } catch (FileNotFoundException e) {
                    JOptionPane.showMessageDialog(this, "File \"" + file.getName() + "\" not found");
                } catch (IOException e) {
                    JOptionPane.showMessageDialog(this, "Error opening file: " + e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please select a valid .txt file");
            }
        }
    }//GEN-LAST:event_OpenMenuItemActionPerformed

    private void ProsesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProsesButtonActionPerformed
        // TODO add your handling code here:
        String text = InputTextArea.getText();
        int karakter = text.replaceAll("\\s+", "").length();
        int baris = text.split("\n").length;
        int vokal = text.replaceAll("[^AEIOUaeiou]", "").length();
        int konsonan = text.replaceAll("[^a-zA-Z]", "").length() - vokal;;

        KarakterTextField.setText(String.valueOf(karakter));
        BarisTextField.setText(String.valueOf(baris));
        VokalTextField.setText(String.valueOf(vokal));
        KonsonanTextField.setText(String.valueOf(konsonan));
    }//GEN-LAST:event_ProsesButtonActionPerformed

